p = "Paragraphs"
y = "You"

m = "Mother"
b = "Brother"

s = "Sister"
c = "Cousin"

j = "Joke"
l = "Love"
i = "I"

# Regular "+" Operator
print("I" + " " "Love" + " " "You")

# Join Operator
print(" ".join([j, l]))

# Format Operator
print("{} {}".format(s,c))

# Modulo Operator/ % Formater
print("%s %s" % (m, b))

# f string
print(f"{p} {y}")

print(f"{i} {l} {y}")